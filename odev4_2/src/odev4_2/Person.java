package odev4_2;

public class Person {
	int nationalId;
	String firstName;
	String lastName;
	String dateOfBirth;
	
	public Person(int nationalId, String firstName, String lastName, String dateOfBirth) {
		super();
		this.nationalId = nationalId;
		this.firstName = firstName;
		this.lastName = lastName;
		this.dateOfBirth = dateOfBirth;
	}
	
	
}
